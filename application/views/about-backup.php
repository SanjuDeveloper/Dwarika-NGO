<p class="text-justify">The folk culture of India is the soul of India! For centuries, our intangible heritage has been passed down from generation to generation through oral traditions. There is a need to conduct regular workshops, interactive presentations, lectures, seminars and exhibitions in schools, colleges, universities and institutes to create awareness among the new generation about our rich folk culture and its performance formats. It is also necessary to have continuous research and survey on our performing folk arts. Publication of findings from such research and surveys also helps lend to the same. It is a cyclical and a continuous process. Thus it is proposed that every school going child at the primary level educates and sensitizes us about our great Indian culture through the folk arts of India, living in rural parts of India. We at Dwarka Club Federation are committed to preserving the rich cultural traditions of our great country as they play an important role in the spread of social values and ethics, spiritual and philosophical ideology and we believe that the new generation of India should share their folklore Connecting with folk, dance, folk music, folk traditions will create a sense of pride for Indian culture among themWe will leave no stone unturned in our goal of preserving and popularizing our intangible heritage. All forms of folk and tribal culture such as folk music, folk dance, folk theater; Our objectives include folk paintings, folk literature, folk handicrafts, folk instruments and folk traditions.</p>
