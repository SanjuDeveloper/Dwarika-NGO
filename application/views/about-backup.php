		 <h2 class="text-center my-4">From The Director’s Desk</h2>
		 <h4>Women Empowerment :</h4><br>
		 <p class="text-justify">At Dwarka Club Federation, we believe that every woman has the right and is entitled to live a dignified and quality life, regardless of her education, her socio-economic background, her marital status, or any other factor that ever exists for society. A barrier may have been established. With increasing cases of violence against women, activating the non-frugal count of domestic and otherwise, female feticide and infanticide, non-female literacy, unequal pay at work, sexual harassment at work, child marriage and many other abusive practices Is promoted around us. Therefore, we follow a model of post-implementation awareness for women empowerment. Women in our society have been held back in more ways than cutting off from education, media, or any other self-development and self-nurturing activities in any form. Knowledge is power and therefore, we have taken this first step to empower women to nurture them with information and awareness on various provisions and claim them for self-development.</p><br>
		 <h3>Education :</h3><br>
		 <p class="text-justify">Education is the most basic criteria or the cornerstone of a developed society. The pure rote learning of theorems and literature for education is not learning, but one that focuses on the development of the mind. It is one that helps a person achieve his maximum potential within a given set of resources and shapes him into a well-rounded human being. Education is that which not only teaches its student to take away from the society but gives back to the same society, maintains the society and contributes to its development. Values are an inseparable part of a good education and should be established in the mind of the students, but they should be given the freedom to use those values in their own different way. Any correct value inserted in a child will not be misunderstood or misquoted. A holistic education will ensure that children do not turn into money mining robots but humans who bring to life the lives of one or the other humans.

As per the 2011 census, Delhi has a literacy rate of 86.3 percent, with a male literacy rate of 91 percent, while the female literacy rate is only 80.9 percent, which is closer to the national literacy rate of 74.04 percent. This shows that female literacy remains a major challenge in the national capital. This phenomenon can be attributed to many factors that are nature as well as cultural and religious socio-economic.
Female literacy has a direct impact on the growth of the economy among other things like other literacy rate, malnutrition etc. These figures show us primarily that we still have a long way to go to achieve 100 percent literacy, which has been continued by the United Nations. Development Goal 2030 as well as our national aspiration for a bright and shining India. We as citizens of this country have a duty to our fellow citizens to ensure that all (men, women, children, transgenders) get fair opportunities for growth and development. And education is to ensure the achievement of primary growth and development that we want and aspire to.
Therefore, while recognizing this important goal, we at Dwarka Club Federation are promoting literacy and education through various awareness programs, seminars, interactive sessions so that beneficiaries realize the importance of sending both girls and boys to school and We also brought through various programs. To enable children from poor socio-economic backgrounds to attend school.

Health
Health is an essential component of being human; Both positive and negative perceptions have a deep resonance with the human psyche. When we are sick, hurt, dying, the consequences are beyond personal, affecting the well-being of families, communities, the economy, and our nation. We have heard many times that India is a country of iron. And this irony is also seen in the health sector. On the one hand we have people from all over the world coming to India for treatment, but on the other hand we have people dying due to low quality, sub-standard or inadequate health services. If our people are to contribute and benefit from a productive economy, then we must change our health system in a way that sees health and other sectors as mutually and important for the well-being of our society.</p><br>
